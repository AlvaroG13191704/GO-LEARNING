package main

import "fmt"

func main() {
	// assigment operator
	x := 2
	x += 2 // add
	fmt.Println(x)
	x -= 2 // sub
	fmt.Println(x)
	x *= 2 // mul
	fmt.Println(x)
	x /= 2 // div
	fmt.Println(x)
	x %= 2 // mod
	fmt.Println(x)

	// increment and decrement
	x++ // x = x + 1
	fmt.Println(x)
	x-- // x = x - 1
	fmt.Println(x)

}
